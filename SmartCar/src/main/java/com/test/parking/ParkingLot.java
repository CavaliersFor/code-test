package com.test.parking;

/**
 * 停车场类
 * @author Benson
 * @date 2019-4-26
 */
public class ParkingLot {
    private int x;
    private int y;

    public ParkingLot(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
